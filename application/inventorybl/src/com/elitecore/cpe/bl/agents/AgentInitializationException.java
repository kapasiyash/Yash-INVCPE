package com.elitecore.cpe.bl.agents;





public class AgentInitializationException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public AgentInitializationException() {
		super("Agent initialization failed");
	}
	
	public AgentInitializationException(String message) {
		super(message);
	}
	
	public AgentInitializationException(Throwable throwable) {
		super(throwable);
	} 
	
	public AgentInitializationException(String message, Throwable throwable) {
		super(message, throwable);
	}
	
}
