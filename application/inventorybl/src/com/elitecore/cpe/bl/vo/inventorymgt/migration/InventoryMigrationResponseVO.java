package com.elitecore.cpe.bl.vo.inventorymgt.migration;

import java.io.Serializable;

public class InventoryMigrationResponseVO implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	
	private Long validEntry;
	private Long invalidEntry;
	private String batchNo;
	
	
	public Long getValidEntry() {
		return validEntry;
	}
	public void setValidEntry(Long validEntry) {
		this.validEntry = validEntry;
	}
	public Long getInvalidEntry() {
		return invalidEntry;
	}
	public void setInvalidEntry(Long invalidEntry) {
		this.invalidEntry = invalidEntry;
	}
	public String getBatchNo() {
		return batchNo;
	}
	public void setBatchNo(String batchNo) {
		this.batchNo = batchNo;
	}
	@Override
	public String toString() {
		return "InventoryMigrationResponseVO [validEntry=" + validEntry
				+ ", invalidEntry=" + invalidEntry + ", batchNo=" + batchNo
				+ "]";
	}
	
	
	
	
	
	

}
