package com.elitecore.cpe.bl.exception;


public class CreateBLException extends Exception {
	
	private static final long serialVersionUID = 1L;	

	public CreateBLException() {
		super("Exception while Persisting entity");
	}
	
	public CreateBLException(String message) {
		super(message);
	}
	
	public CreateBLException(Throwable throwable) {
		super(throwable);
	} 
	
	public CreateBLException(String message, Throwable throwable) {
		super(message, throwable);
	}	
}

