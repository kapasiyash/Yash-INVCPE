package com.elitecore.cpe.bl.ws.data.input.request;

import java.io.Serializable;
import java.util.List;

import com.elitecore.cpe.bl.ws.data.input.vo.InventoryAttributeVO;

public class InventoryDetailsRequestData implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	
	private String wareHouseName;
	private String wareHouseCode;
	private String resourceName;
	private String resourceType;
	private String model;
	
	private String resourceId;
	private String inventoryNumber;
	private String inventoryStatus;
	private String transferStatus;
	
	private List<InventoryAttributeVO> attributeVOs;

	
	
	public String getWareHouseCode() {
		return wareHouseCode;
	}

	public void setWareHouseCode(String wareHouseCode) {
		this.wareHouseCode = wareHouseCode;
	}

	public String getWareHouseName() {
		return wareHouseName;
	}

	public void setWareHouseName(String wareHouseName) {
		this.wareHouseName = wareHouseName;
	}

	public String getResourceName() {
		return resourceName;
	}

	public void setResourceName(String resourceName) {
		this.resourceName = resourceName;
	}

	public String getModel() {
		return model;
	}

	public void setModel(String model) {
		this.model = model;
	}

	public String getResourceId() {
		return resourceId;
	}

	public void setResourceId(String resourceId) {
		this.resourceId = resourceId;
	}

	public String getResourceType() {
		return resourceType;
	}

	public void setResourceType(String resourceType) {
		this.resourceType = resourceType;
	}

	public String getInventoryNumber() {
		return inventoryNumber;
	}

	public void setInventoryNumber(String inventoryNumber) {
		this.inventoryNumber = inventoryNumber;
	}

	public String getInventoryStatus() {
		return inventoryStatus;
	}

	public void setInventoryStatus(String inventoryStatus) {
		this.inventoryStatus = inventoryStatus;
	}

	public List<InventoryAttributeVO> getAttributeVOs() {
		return attributeVOs;
	}

	public void setAttributeVOs(List<InventoryAttributeVO> attributeVOs) {
		this.attributeVOs = attributeVOs;
	}
	
	public String getTransferStatus() {
		return transferStatus;
	}

	public void setTransferStatus(String transferStatus) {
		this.transferStatus = transferStatus;
	}

	@Override
	public String toString() {
		return "InventoryDetailsRequestData [wareHouseName=" + wareHouseName
				+ ", resourceName=" + resourceName + ", resourceId="
				+ resourceId + ", resourceType=" + resourceType
				+ ", inventoryNumber=" + inventoryNumber + ", inventoryStatus="
				+ inventoryStatus + ", attributeVOs=" + attributeVOs + "]";
	}
	
	
	
	

}
