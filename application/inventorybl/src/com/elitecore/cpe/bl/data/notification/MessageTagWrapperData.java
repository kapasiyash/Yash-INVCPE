package com.elitecore.cpe.bl.data.notification;

import java.io.Serializable;

public class MessageTagWrapperData implements Serializable{

	/**
	 * @author Yash.Kapasi
	 */
	private static final long serialVersionUID = 1L;

	private Long messageTagId;
    private String messageText;
    private String description;
    private String messageTag;
    
    
    
    
	public MessageTagWrapperData(Long messageTagId, String messageText,
			String description, String messageTag) {
		super();
		this.messageTagId = messageTagId;
		this.messageText = messageText;
		this.description = description;
		this.messageTag = messageTag;
	}
	
	
	public Long getMessageTagId() {
		return messageTagId;
	}
	public void setMessageTagId(Long messageTagId) {
		this.messageTagId = messageTagId;
	}
	public String getMessageText() {
		return messageText;
	}
	public void setMessageText(String messageText) {
		this.messageText = messageText;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getMessageTag() {
		return messageTag;
	}
	public void setMessageTag(String messageTag) {
		this.messageTag = messageTag;
	}
    
    
}
