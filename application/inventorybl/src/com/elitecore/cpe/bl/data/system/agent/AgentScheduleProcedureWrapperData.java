package com.elitecore.cpe.bl.data.system.agent;

import java.io.Serializable;
import java.sql.Timestamp;



public class AgentScheduleProcedureWrapperData implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	
	
	private String eventId;
	private String executionInterval;
	private Timestamp nextScheduleDate;
	private Boolean isSchedulable;
	
	
	
	public Boolean getIsSchedulable() {
		return isSchedulable;
	}
	public void setIsSchedulable(Boolean isSchedulable) {
		this.isSchedulable = isSchedulable;
	}
	public Timestamp getNextScheduleDate() {
		return nextScheduleDate;
	}
	public void setNextScheduleDate(Timestamp nextScheduleDate) {
		this.nextScheduleDate = nextScheduleDate;
	}
	public String getEventId() {
		return eventId;
	}
	public void setEventId(String eventId) {
		this.eventId = eventId;
	}
	public String getExecutionInterval() {
		return executionInterval;
	}
	public void setExecutionInterval(String executionInterval) {
		this.executionInterval = executionInterval;
	}
	@Override
	public String toString() {
		return "AgentScheduleProcedureWrapperData [eventId=" + eventId
				+ ", executionInterval=" + executionInterval + "]";
	}

	
	
}
