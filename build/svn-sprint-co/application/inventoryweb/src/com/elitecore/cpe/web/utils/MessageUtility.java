package com.elitecore.cpe.web.utils;

import org.zkoss.zul.Messagebox;

public class MessageUtility {

	public static void successInformation(String strTitle,String strMessage){
		Messagebox.show(strMessage, strTitle, Messagebox.OK, Messagebox.INFORMATION);
	}
	public static void failureInformation(String strTitle,String strMessage){
		Messagebox.show(strMessage, strTitle, Messagebox.OK, Messagebox.ERROR);
	}
}
