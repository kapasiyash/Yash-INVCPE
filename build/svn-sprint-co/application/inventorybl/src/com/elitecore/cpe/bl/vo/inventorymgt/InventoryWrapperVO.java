package com.elitecore.cpe.bl.vo.inventorymgt;

import java.io.Serializable;
import java.util.ArrayList;
import java.util.List;

import com.elitecore.cpe.bl.entity.inventory.inventorymgt.InvalidInventoryData;
import com.elitecore.cpe.bl.entity.inventory.inventorymgt.InventoryData;
import com.elitecore.cpe.bl.entity.inventory.master.AttributeData;
import com.elitecore.cpe.bl.entity.inventory.master.AttributeTransData;

public class InventoryWrapperVO implements Serializable{

	
	private InventoryData inventoryData;
	private InvalidInventoryData invalidInventoryData;
	private List<AttributeTransData> attributeTransDatas;
	
	public InventoryData getInventoryData() {
		return inventoryData;
	}
	public void setInventoryData(InventoryData inventoryData) {
		this.inventoryData = inventoryData;
	}
	public List<AttributeTransData> getAttributeTransDatas() {
		return attributeTransDatas;
	}
	public void setAttributeTransDatas(List<AttributeTransData> attributeTransDatas) {
		this.attributeTransDatas = attributeTransDatas;
	}
	
	
	public InvalidInventoryData getInvalidInventoryData() {
		return invalidInventoryData;
	}
	public void setInvalidInventoryData(InvalidInventoryData invalidInventoryData) {
		this.invalidInventoryData = invalidInventoryData;
	}
	
	@Override
	public String toString() {
		return "InventoryWrapperVO [inventoryData=" + inventoryData
				+ ", invalidInventoryData=" + invalidInventoryData
				+ ", attributeTransDatas=" + attributeTransDatas + "]";
	}
	
	
}
