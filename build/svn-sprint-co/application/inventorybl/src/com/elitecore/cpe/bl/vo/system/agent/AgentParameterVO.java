package com.elitecore.cpe.bl.vo.system.agent;

import java.io.Serializable;
import java.util.Map;

import com.elitecore.cpe.bl.vo.core.expr.ConstraintExpressionVO;


public class AgentParameterVO implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	
	private Long agentparamid;
	private String name;
    private String alias;
    private String customfieldtypeid;
//    private String valuesource;
    private Map<String,String> valueSource;
    private ConstraintExpressionVO  expressonVO;
    
    
    
	public Long getAgentparamid() {
		return agentparamid;
	}
	public void setAgentparamid(Long agentparamid) {
		this.agentparamid = agentparamid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAlias() {
		return alias;
	}
	public void setAlias(String alias) {
		this.alias = alias;
	}
	public String getCustomfieldtypeid() {
		return customfieldtypeid;
	}
	public void setCustomfieldtypeid(String customfieldtypeid) {
		this.customfieldtypeid = customfieldtypeid;
	}
	
	
	public Map<String, String> getValueSource() {
		return valueSource;
	}
	public void setValueSource(Map<String, String> valueSource) {
		this.valueSource = valueSource;
	}
	public ConstraintExpressionVO getExpressonVO() {
		return expressonVO;
	}
	public void setExpressonVO(ConstraintExpressionVO expressonVO) {
		this.expressonVO = expressonVO;
	}
	@Override
	public String toString() {
		return "AgentParameterVO [agentparamid=" + agentparamid + ", name="
				+ name + ", alias=" + alias + ", customfieldtypeid="
				+ customfieldtypeid + ", valueSource=" + valueSource
				+ ", expressonVO=" + expressonVO + "]";
	}
    
	
    

}
